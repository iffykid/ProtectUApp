    private void p2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_p2KeyReleased
        if(p2.getText().equals(p1.getText()) == false){
            errorText.setForeground(Color.red);
            errorText.setText("PASSWORDS DO NOT MATCH");
        }
        else if(p2.getText().equals(p1.getText()) == true){
            errorText.setForeground(Color.GREEN);
            errorText.setText("PASSWORDS MACTH");
            passwordsMatch = true;
        }
    }//GEN-LAST:event_p2KeyReleased

