    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        // TODO add your handling code here:
        /*
        JOptionPane.showMessageDialog(null,"THANKS FOR JOINING....");
        
        */
        nameLabel.setForeground(Color.black);
        lastNameLabel.setForeground(Color.black);
        phoneNumberLabel.setForeground(Color.black);
        emailLabel.setForeground(Color.black);
        cityLabel.setForeground(Color.black);
        age.setForeground(Color.black);
        genderLabel.setForeground(Color.black);
        p1.setForeground(Color.black);
        p2.setForeground(Color.black);
        
        UserClass uc = new UserClass();
        GPSClass gps = new GPSClass();
        int areaCode = gps.areaCodeFinder(city.getText());
        
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\."+"[a-zA-Z0-9_+&*-]+)*@" + "(?:[a-zA-Z0-9-]+\\.)+[a-z" + "A-Z]{2,7}$";
        Pattern pat = Pattern.compile(emailRegex);
        
        if(fname.getText().length() < 2){
            nameLabel.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "MISSING FIELDS","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
        }
        else if(lname.getText().length() < 2){
            lastNameLabel.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "MISSING FIELDS","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
        }
        else if(pnumber.getText().length() < 9){
            phoneNumberLabel.setForeground(Color.red);
            Scanner sc = new Scanner(pnumber.getText());
            if(!sc.hasNextInt()){
                phoneNumberLabel.setForeground(Color.red);
            }
            phoneNumberLabel.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "MISSING/INVALID NUMBER","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
        }
        else if(ecnumber.getText().length() < 9){
            Scanner sc = new Scanner(ecnumber.getText());
            if(!sc.hasNextInt()){
                emergencyNumberLabel.setForeground(Color.red);
            }
            emergencyNumberLabel.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "MISSING/INVALID NUMBER","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
        }
        else if(pat.matcher(email.getText()).matches() == false){
            emailLabel.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, email.getText() + " IS NOT A VALID EMAIL","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
        }
        else if(age.getValue().toString().equals(0)){
            JOptionPane.showMessageDialog(null, "PLEASE SELECT VALID AGE","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
            age.setForeground(Color.red);
        }
        else if(gender.getText().length() < 3){
            JOptionPane.showMessageDialog(null, "PLEASE ENTER GENDER'","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
            genderLabel.setForeground(Color.red);
        }
        else if(areaCode == 0){
            cityLabel.setForeground(Color.red);
        }
        else if(p1.getText().length() < 3 && p2.getText().length() < 3){
            passwordLabel.setForeground(Color.red);
            confirmLabel.setForeground(Color.red);
        }
        else if(passwordsMatch == false){
            JOptionPane.showMessageDialog(null, "PASSWORDS DO NOT MATCH PLEASE TRY AGAIN","TRY AGAIN", JOptionPane.PLAIN_MESSAGE);
        }
        else{
            
            uc.newUser(fname.getText(),lname.getText(), pnumber.getText(),email.getText(),areaCode , ecnumber.getText(),Integer.parseInt(age.getValue().toString()), gender.getText(), p2.getText());
            Login lg = new Login();
            lg.setVisible(true);
            dispose();
        }
        
        //uc.newUser(fname.getText(), lname.getText(), pnumber.getText(), p2.getText(),1, address.getText(), ecnumber.getText() , Integer.parseInt(age.getValue().toString()), gender.getText());
    }//GEN-LAST:event_signUpActionPerformed

